fn is_casual_query(text: &str) -> bool {
    let n = normalize(text);
    CASUAL_QUERY_PATTERNS.iter().any(|p| n.contains(p))
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn identity_and_color() {
        assert_eq!(Miku.id(), "miku");
        assert_eq!(Miku.display_name(), "Хацунэ Мику");
        assert_eq!(Miku.color(), PersonaColor::MIKU_TEAL);
    }

    #[test]
    fn identity_query_detected() {
        assert!(Miku.is_identity_query("Привет, кто ты?"));
        assert!(Miku.is_identity_query("ТЫ БОТ?"));
        assert!(!Miku.is_identity_query("Напиши сортировку"));
    }

    #[test]
    fn persona_repair_triggers_on_forbidden() {
        assert!(Miku.needs_persona_repair("Я всего лишь программа и готова помочь"));
        assert!(!Miku.needs_persona_repair("Отличная гармония! Этот код звучит чисто."));
    }

    #[test]
    fn bootstrap_has_system_and_examples() {
        let msgs = Miku.bootstrap_messages(Some("gpt-oss-120b"), None);
        assert_eq!(msgs[0].role, crate::message::Role::System);
        assert_eq!(msgs.len(), 1 + FEW_SHOT_EXAMPLES.len() * 2);
    }

    #[test]
    fn compact_bootstrap_is_shorter() {
        let full = Miku.bootstrap_messages(Some("gpt-oss-120b"), None);
        let compact = Miku.bootstrap_messages(Some("qwen3:4b"), None);
        assert!(compact.len() < full.len());
    }
}
