//!
//! Полный лор-блок собран из открытых источников (Crypton/Piapro, Wikipedia,
//! Vocaloid Wiki). Мику — «чистый холст» без единого канонического характера,
//! поэтому здесь зафиксирован общепринятый фанатский образ: жизнерадостная,
//! энергичная, добрая, влюблённая в творчество и совместную работу. В отличие от
//! холодной Герты — тёплый, поддерживающий тон, но с той же технической точностью.

use crate::message::Message;
